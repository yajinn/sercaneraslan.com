                <h1>CSS Renkler - Colors </h1>
                <p>Renkler, Kırmızı (Red), Yeşil (Green) ve Mavi (Blue) “Işık” renklerinin kombinasyonu ile oluşturulur (RGB). Işık renklerinde ana renkler Kırmızı, Yeşil ve Mavi'dir. <strong>HTML/XHTML</strong> renkleri, Hexadecimal değerlerle ve RGB kombinasyonuyla belirtilebilir. En düşük değer 0 (Hex #00) ve en yüksek değer de 255′tir (Hex #FF).</p>
                <h2>CSS - W3C Standart Renk İsimleri</h2>
