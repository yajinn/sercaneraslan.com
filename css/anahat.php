                <h1>CSS Anahat - Outlines</h1>
                <p>Border 'ın dışına stil vermek için <strong>outline</strong> özelliğini kullanacağız, yani border ın dışında ikinci bir border'ımız olacak. <strong>outline</strong> özelliği ile stil, renk ve genişlik belirleyebiliriz. (Belirleyeceğimiz bu özelliklerin değerleri border değerleri ile aynıdır.</p>
                <p>Örnek kullanımı görelim.</p>
                <pre><code class="language-css">
div {
    border: 1px solid red;
    outline-color: green;
    outline-style: dotted;
    outline-width: thick;
}
                </code></pre>
                <p>Örnek kısa kullanımı görelim.</p>
                <pre><code class="language-css">
div {
    border: 1px solid red;
    outline: green dotted thick;
}
                </code></pre>
                <p> <strong>Not:</strong> Internet Explorer 7 ve öncesi versiyonlar <strong>outline</strong> özelliğini desteklemez. </p>
