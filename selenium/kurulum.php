	        <h1>Selenium IDE Nasıl Kurulur ?</h1> 
                <p>Selenium IDE bir Firefox eklentisi olduğu için öncelikle bir Firefox yüklememiz gerekmektedir. Firefox'u <a href="http://www.mozilla-europe.org/tr/firefox/">buradan</a> yükleyebilirsiniz.</p>
                <p>Firefox kurulduktan sonra Selenium IDE'yi de <a href="http://seleniumhq.org/download/">buradan</a> yükleyebilirsiniz.</p>
                <p>Yükleme işlemleri bitti, Selenium IDE 'yi biraz daha tanımak için " Devam Et " butonuna tıklayabilirsiniz.</p>
                <div class="note"> 
		      <p><strong>Not:</strong> Selenium IDE yüklemesi bittiğinde Firefox menüsünden sırasıyla "Araçlar > Selenium IDE" linklerine tıklanarak Selenium IDE'ye ulaşılabilir.</p>		
		</div>
