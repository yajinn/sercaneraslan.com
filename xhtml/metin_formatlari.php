                <h1>XHTML Metin Formatları - Formatting</h1>
                <p> Bu bölümde metinleri nasıl şekillendirebileceğimizi göreceğiz. </p>
                <ul>
                    <li> Metni kalın (vurgulu) hale çevirmek için  <strong>" &lt;strong&gt; "</strong> etiketini kullanacağız.</li>
                    <li> Metni büyük yapmak için  <strong>" &lt;big&gt; "</strong> etiketini kullanacağız.</li>
                    <li> Metni küçük yapmak için  <strong>" &lt;small&gt; "</strong> etiketini kullanacağız.</li>
                    <li> Metni yatık hale çevirmek için  <strong>" &lt;i&gt; "</strong> etiketini kullanacağız.</li>
                    <li> Metinde alt simge, rakam, harf vs kullanmak istiyorsak <strong>" &lt;sub&gt; "</strong> etiketini kullanacağız.</li>
                    <li> Metinde üst simge, rakam, harf vs kullanmak istiyorsak <strong>" &lt;sup&gt; "</strong> etiketini kullanacağız.</li>   
                </ul>
                <div class="note"> 
                    <p> <strong>Not:</strong> Yukarıda ki etiketler "p", "h1", "h2", "h3", "h4", "h5", "h6", "div", "pre", "address", "fieldset", "ins", "del" gibi etiketler içinde kullanılmaıldır. Aksi takdirde kodumuz hata verecektir. </p>
                </div>
                <p> <strong>Örnek olarak görelim...</strong> </p> <br />
                <pre><code class="language-javascript">
