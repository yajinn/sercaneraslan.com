                <h1>HTML5 Yeni Input Tipleri</h1>
                <p>HTML5 formları için birçok yeni input tipleri gelmiştir. Bu yeni özellikler, iyi bir input kontrolü ve doğrulaması sağlar.</p>
                <p>Not: Tüm gelişmiş tarayıcılar yeni input tiplerini destekler. Ancak tarayıcılar yeni input tiplerini desteklemiyorsa bunlar normal text alanları gibi gözükecektir.</p>
                <p>Yeni input tipleri:</p>
                <p><strong>color</strong> (renkler için)</p>
                <pre><code class="language-javascript">
&lt;input type="color" name="favoriRenk" /&gt;          
                </code></pre>
                <p><strong>date</strong> (doğumgünü, yıldönümü vs için)</p>
                <pre><code class="language-javascript">
&lt;input type="date" name="dogumgunu" /&gt;          
                </code></pre>   
                <p><strong>datetime</strong> (date tipine ek olarak saati de kapsar)</p>
                <pre><code class="language-javascript">
&lt;input type="datetime" name="dogumgunuSaat" /&gt;          
                </code></pre>   
                <p><strong>datetime-local</strong> (datetime tipine ek olarak zaman dilimi tanımlar)</p>
                <pre><code class="language-javascript">
&lt;input type="datetime-local" name="dogumgunuSaatZamanDilimi" /&gt;          
                </code></pre>       
                <p><strong>email</strong> (e-mailler için)</p>
                <pre><code class="language-javascript">
&lt;input type="email" name="kullaniciEmail" /&gt;          
                </code></pre>   
                <p><strong>month</strong> (ay bilgisini yazmak için)</p>
                <pre><code class="language-javascript">
&lt;input type="month" name="Ay" /&gt;          
                </code></pre>       
                <p><strong>number</strong> (sadece rakam  yazmak için. min, max, step ve value öznitelikleri tanımlanabilir.)</p>
                <pre><code class="language-javascript">
&lt;input type="number" name="adet" min="1" max="5" /&gt;          
                </code></pre>
                <p><strong>range</strong> (Fiyat aralığı vb yazmak için. min, max, step ve value öznitelikleri tanımlanabilir.)</p>
                <pre><code class="language-javascript">
&lt;input type="range" name="fiyat" min="1" max="10" /&gt;          
                </code></pre>
                <p><strong>search</strong> (arama inputları için.)</p>
                <pre><code class="language-javascript">
&lt;input type="search" name="arama" /&gt;          
                </code></pre>
                <p><strong>tel</strong> (telefon yazmak için)</p>
                <pre><code class="language-javascript">
&lt;input type="tel" name="kullaniciTelefonu" /&gt;          
                </code></pre>
                <p><strong>time</strong> (Saat için)</p>
                <pre><code class="language-javascript">
&lt;input type="time" name="saat" /&gt;          
                </code></pre>
                <p><strong>url</strong> (url girebilmek için)</p>
                <pre><code class="language-javascript">
&lt;input type="url" name="anasayfa" /&gt;          
                </code></pre>
                <p><strong>week</strong> (hafta bilgisini yazmak için)</p>
                <pre><code class="language-javascript">
&lt;input type="week" name="hafta" /&gt;          
                </code></pre>
